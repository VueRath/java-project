    private void WithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WithdrawActionPerformed
        try {
            String toWithdraw = JOptionPane.showInputDialog(null,"Please Enter an amount to Withdraw: ","Withdrawal",JOptionPane.PLAIN_MESSAGE);
            if(toWithdraw.isEmpty()){
                JOptionPane.showMessageDialog(null,"What?????","Error",JOptionPane.ERROR_MESSAGE);
            }
            double WithdrawedMoney = Double.parseDouble(toWithdraw);
            if(WithdrawedMoney > addMoney){
               JOptionPane.showMessageDialog(null,"Invalid Withdrawal!!","Invalid",JOptionPane.ERROR_MESSAGE);

            } else if(WithdrawedMoney < 0){
                JOptionPane.showMessageDialog(null,"Withdrawal money cannot be negative!!","Invalid",JOptionPane.ERROR_MESSAGE);
            } else {
                addMoney = addMoney - WithdrawedMoney;
                SavingsAccount.append("\t          You Withdrawed: " + String.format("%.2f", WithdrawedMoney) + "$");
                SavingsAccount.append("\n");
                
            }
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Invalid Inputation!","Invalid",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_WithdrawActionPerformed

