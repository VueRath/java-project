    private void DepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepositActionPerformed
      try {
        String inputMoney = JOptionPane.showInputDialog(null,"Please Enter an Amount: ","Depositing Money",JOptionPane.PLAIN_MESSAGE);
        if(inputMoney.isEmpty()){
            JOptionPane.showMessageDialog(null,"What?????","Error",JOptionPane.ERROR_MESSAGE);
        }
      Money = Double.parseDouble(inputMoney);
      
      
      
      if(Money < 0){
          JOptionPane.showMessageDialog(null, "Money Cannot be negative!","Invalid",JOptionPane.ERROR_MESSAGE);
      } else {
      addMoney += Money;
      
      SavingsAccount.append("\t          You Deposited: " + String.format("%.2f", Money) + "$");
      SavingsAccount.append("\n");
      }
      } catch(NumberFormatException e){
          JOptionPane.showMessageDialog(null, "Invalid Inputation!","Invalid",JOptionPane.ERROR_MESSAGE);
      } 
     
    }//GEN-LAST:event_DepositActionPerformed

